#include<stdio.h>

/*  code for typecasting(conversion of one datatype into another datatype)
    Typecasting syntax
    (type) value;
 */
int main()
{  int a = 3;
     float b = 54;
     float B = 37/2;
     float c = (float)37/2;
    printf("1.The value of b =%d\n",b);
    printf("2.The value of b =%f\n",b);
    printf("3.The value of b =%f\n",B);
    printf("4.The value of b =%f\n",c);

    return 0;
}
